/**
 * An enumeration of the different types of terrain in the world.
 * @author Zachary Palmer
 */
public enum Terrain
{
	/**
	 * The enumerative type for normal ground.
	 */
	GROUND,
	/**
	 * The enumerative type for rough ground.
	 */
	ROUGH,
	/**
	 * The enumerative type for a wall.
	 */
	WALL
}
